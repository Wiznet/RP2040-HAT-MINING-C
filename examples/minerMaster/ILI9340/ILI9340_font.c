unsigned char char_offset(char c)
{
    if (c > 126 || c < 33) c = 33;

    return (unsigned char) c - 33;
}


unsigned short getCharAddress(char c)
{
    if (c > 126 || c < 32) c = 32;
    
    return (unsigned char) (c - 32)* 32;
}

unsigned short line_width(char *c)
{
    unsigned short w = 0;

    unsigned short i=0;

    while (c[i] != 0)
    {
    	if (c[i] == '\n')
    	{
    		break; //end of line
    	}
    	else if (c[i] == ' ') //space
    	{
    		w += FONT_SPACE_SIZE;
    	}
    	else if (c[i] > 32 && c[i] < 127) // valid range
    	{
    		w += char_width(c[i]);

    		w += FONT_CHAR_GAP;
    	}

    	i++; //increment character
    }

    return w;
}


